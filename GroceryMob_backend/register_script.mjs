const lines = accounts.split('\n').map(l => l.trim()).filter(l => l);

async function run() {
  let successCount = 0;
  for (let line of lines) {
    const parts = line.split('\t');
    if (parts.length >= 4) {
      const name = parts[0].trim();
      let emailRaw = parts[1].trim();
      if (!emailRaw.includes('@')) {
        emailRaw += '@example.com';
      }
      const phone = parts[2].trim();
      const password = parts[3].trim();
      
      try {
        const res = await fetch('http://localhost:5000/api/auth/register', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ name, email: emailRaw, phone, password })
        });
        const data = await res.json();
        if (data.success) {
          successCount++;
          console.log(`Registered: ${emailRaw}`);
        } else {
          console.error(`Failed to register ${emailRaw}:`, data);
        }
      } catch (err) {
        console.error(`Error registering ${emailRaw}: ${err.message}`);
      }
    }
  }
  console.log(`Successfully registered ${successCount} out of ${lines.length} accounts.`);
}
run();
